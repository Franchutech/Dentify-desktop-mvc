package com.example.dentify.Model;

/**
 * Define el conjunto de especialidades odontológicas disponibles en la clínica.
 * Se utiliza para clasificar la actividad profesional de los doctores y estructurar
 * el renderizado de estilos en la interfaz visual de las tablas médicas.
 */
public enum Especialidad  {

    /**
     * Especialidad enfocada en el tratamiento de conductos radiculares y pulpa dental.
     */
    ENDODONCITAS,

    /**
     * Especialidad encargada del estudio, prevención y corrección de las anomalías de forma y posición de los dientes.
     */
    ORTODONCISTA,

    /**
     * Especialidad dedicada al diagnóstico y tratamiento quirúrgico de enfermedades, heridas y aspectos estéticos de la boca y maxilares.
     */
    CIRUGIA,

    /**
     * Odontología general encargada del diagnóstico primario, prevención y tratamientos básicos del paciente.
     */
    GENERAL,

    /**
     * Especialidad encargada de la prevención, diagnóstico y tratamiento de las enfermedades que afectan a las encías y tejidos de soporte.
     */
    PERIODONCISTA,

}